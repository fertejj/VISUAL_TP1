package ar.edu.unju.fi.ejercicio1;

public class Main {
    public static void main(String[] args) {
        // Declaración de variables enteras y asignación de valores
        int num1 = 8;
        int num2 = 5;
        int num3 = 1;
        int num4 = 20;
        int num5 = 9;

        // Calculo del promedio
        double promedio = (num1 + num2 + num3 + num4 + num5) / 5.0;  // Uso de 5.0 para obtener un resultado de tipo double

        // Mostrar el resultado por consola
        System.out.println("El promedio es: " + promedio);
    }
}
